export function getItemTrackingIds(item, trackLevel) {
  if (trackLevel) {
    return [`${item.id}:collected`, `${item.id}:leveled`];
  }
  return [item.id];
}

export function getAllItemIds() {
  const ids = [];
  for (const [, section] of Object.entries(gameData)) {
    for (const category of Object.values(section.categories)) {
      for (const item of category.items) {
        ids.push(...getItemTrackingIds(item, section.trackLevel));
      }
    }
  }
  return ids;
}

export function getTotalItems() {
  return getAllItemIds().length;
}

export function getSectionItemIds(sectionKey) {
  const section = gameData[sectionKey];
  if (!section) return [];
  const ids = [];
  for (const category of Object.values(section.categories)) {
    for (const item of category.items) {
      ids.push(...getItemTrackingIds(item, section.trackLevel));
    }
  }
  return ids;
}

export function getCategoryItemIds(sectionKey, categoryKey) {
  const section = gameData[sectionKey];
  if (!section) return [];
  const category = section.categories[categoryKey];
  if (!category) return [];
  const ids = [];
  for (const item of category.items) {
    ids.push(...getItemTrackingIds(item, section.trackLevel));
  }
  return ids;
}
